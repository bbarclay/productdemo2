package com.cg.digi.model;

/**
 * @author hapemmas
 *
 */
public class Task {

	
	private String taskid;
	  private String taskname;
	  private String senario;
	  private String starturl;
	  private String successurl;
	  private String createdby;
	  private String creationtime;
	  private String modifiedby;
	  private String modifiedtime;
	  private String surveyid;
	  private String status;
	  private String userid;
	  
	  
	  public Task() {
	}


	/**
	 * @return the taskid
	 */
	public String getTaskid() {
		return taskid;
	}


	/**
	 * @param taskid the taskid to set
	 */
	public void setTaskid(String taskid) {
		this.taskid = taskid;
	}


	/**
	 * @return the taskname
	 */
	public String getTaskname() {
		return taskname;
	}


	/**
	 * @param taskname the taskname to set
	 */
	public void setTaskname(String taskname) {
		this.taskname = taskname;
	}


	/**
	 * @return the senario
	 */
	public String getSenario() {
		return senario;
	}


	/**
	 * @param senario the senario to set
	 */
	public void setSenario(String senario) {
		this.senario = senario;
	}


	/**
	 * @return the starturl
	 */
	public String getStarturl() {
		return starturl;
	}


	/**
	 * @param starturl the starturl to set
	 */
	public void setStarturl(String starturl) {
		this.starturl = starturl;
	}


	/**
	 * @return the successurl
	 */
	public String getSuccessurl() {
		return successurl;
	}


	/**
	 * @param successurl the successurl to set
	 */
	public void setSuccessurl(String successurl) {
		this.successurl = successurl;
	}


	/**
	 * @return the createdby
	 */
	public String getCreatedby() {
		return createdby;
	}


	/**
	 * @param createdby the createdby to set
	 */
	public void setCreatedby(String createdby) {
		this.createdby = createdby;
	}


	/**
	 * @return the creationtime
	 */
	public String getCreationtime() {
		return creationtime;
	}


	/**
	 * @param creationtime the creationtime to set
	 */
	public void setCreationtime(String creationtime) {
		this.creationtime = creationtime;
	}


	/**
	 * @return the modifiedby
	 */
	public String getModifiedby() {
		return modifiedby;
	}


	/**
	 * @param modifiedby the modifiedby to set
	 */
	public void setModifiedby(String modifiedby) {
		this.modifiedby = modifiedby;
	}


	/**
	 * @return the modifiedtime
	 */
	public String getModifiedtime() {
		return modifiedtime;
	}


	/**
	 * @param modifiedtime the modifiedtime to set
	 */
	public void setModifiedtime(String modifiedtime) {
		this.modifiedtime = modifiedtime;
	}


	/**
	 * @return the surveyid
	 */
	public String getSurveyid() {
		return surveyid;
	}


	/**
	 * @param surveyid the surveyid to set
	 */
	public void setSurveyid(String surveyid) {
		this.surveyid = surveyid;
	}


	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}


	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}


	/**
	 * @return the userid
	 */
	public String getUserid() {
		return userid;
	}


	/**
	 * @param userid the userid to set
	 */
	public void setUserid(String userid) {
		this.userid = userid;
	}


	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "Task ["
				+ (taskid != null ? "taskid=" + taskid + ", " : "")
				+ (taskname != null ? "taskname=" + taskname + ", " : "")
				+ (senario != null ? "senario=" + senario + ", " : "")
				+ (starturl != null ? "starturl=" + starturl + ", " : "")
				+ (successurl != null ? "successurl=" + successurl + ", " : "")
				+ (createdby != null ? "createdby=" + createdby + ", " : "")
				+ (creationtime != null ? "creationtime=" + creationtime + ", "
						: "")
				+ (modifiedby != null ? "modifiedby=" + modifiedby + ", " : "")
				+ (modifiedtime != null ? "modifiedtime=" + modifiedtime + ", "
						: "")
				+ (surveyid != null ? "surveyid=" + surveyid + ", " : "")
				+ (status != null ? "status=" + status + ", " : "")
				+ (userid != null ? "userid=" + userid : "") + "]";
	}

 
	  
}
