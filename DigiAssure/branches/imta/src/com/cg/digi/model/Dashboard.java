package com.cg.digi.model;

public class Dashboard {
private String key;
private String value;
public Dashboard() {
	super();
}
public String getKey() {
	return key;
}
public void setKey(String key) {
	this.key = key;
}
public String getValue() {
	return value;
}
public void setValue(String value) {
	this.value = value;
}
public Dashboard(String key, String value) {
	super();
	this.key = key;
	this.value = value;
}
@Override
public String toString() {
	return "Dashboard [key=" + key + ", value=" + value + "]";
}

}
