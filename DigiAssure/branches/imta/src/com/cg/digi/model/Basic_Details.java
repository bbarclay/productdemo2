package com.cg.digi.model;

public class Basic_Details {

	private int sr_no;
	private int vend_no;
	private int os_no;
	private int ver_no;
	private String ver_name;
	private String dev_name;
	private String tp;
	private String resol;
	
	public int getSr_no() {
		return sr_no;
	}
	public void setSr_no(int sr_no) {
		this.sr_no = sr_no;
	}
	public int getVend_no() {
		return vend_no;
	}
	public void setVend_no(int vend_no) {
		this.vend_no = vend_no;
	}
	public int getOs_no() {
		return os_no;
	}
	public void setOs_no(int os_no) {
		this.os_no = os_no;
	}
	public int getVer_no() {
		return ver_no;
	}
	public void setVer_no(int ver_no) {
		this.ver_no = ver_no;
	}
	public String getDev_name() {
		return dev_name;
	}
	public void setDev_name(String dev_name) {
		this.dev_name = dev_name;
	}
	public String getTp() {
		return tp;
	}
	public void setTp(String tp) {
		this.tp = tp;
	}
	public String getResol() {
		return resol;
	}
	public void setResol(String resol) {
		this.resol = resol;
	}
	
	public String getVer_name() {
		return ver_name;
	}
	public void setVer_name(String ver_name) {
		this.ver_name = ver_name;
	}
	@Override
	public String toString() {
		return "Basic_Details [sr_no=" + sr_no + ", vend_no=" + vend_no
				+ ", os_no=" + os_no + ", ver_no=" + ver_no + ", ver_name="
				+ ver_name + ", dev_name=" + dev_name + ", tp=" + tp
				+ ", resol=" + resol + "]";
	}
	
}
