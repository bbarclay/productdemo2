package com.cg.digi.model;

public class OsDetails {

private String idosdetails;
private String osname;
private String osdetails;
private String status;
private String creationtime;
private String createdby;
private String modificationtime;
private String modifiedby;
public OsDetails() {
	super();
}
public OsDetails(String idosdetails, String osname, String osdetails, String status, String creationtime,
		String createdby, String modificationtime, String modifiedby) {
	super();
	this.idosdetails = idosdetails;
	this.osname = osname;
	this.osdetails = osdetails;
	this.status = status;
	this.creationtime = creationtime;
	this.createdby = createdby;
	this.modificationtime = modificationtime;
	this.modifiedby = modifiedby;
}
public String getIdosdetails() {
	return idosdetails;
}
public void setIdosdetails(String idosdetails) {
	this.idosdetails = idosdetails;
}
public String getOsname() {
	return osname;
}
public void setOsname(String osname) {
	this.osname = osname;
}
public String getOsdetails() {
	return osdetails;
}
public void setOsdetails(String osdetails) {
	this.osdetails = osdetails;
}
public String getStatus() {
	return status;
}
public void setStatus(String status) {
	this.status = status;
}
public String getCreationtime() {
	return creationtime;
}
public void setCreationtime(String creationtime) {
	this.creationtime = creationtime;
}
public String getCreatedby() {
	return createdby;
}
public void setCreatedby(String createdby) {
	this.createdby = createdby;
}
public String getModificationtime() {
	return modificationtime;
}
public void setModificationtime(String modificationtime) {
	this.modificationtime = modificationtime;
}
public String getModifiedby() {
	return modifiedby;
}
public void setModifiedby(String modifiedby) {
	this.modifiedby = modifiedby;
}
@Override
public String toString() {
	return "OsDetails [idosdetails=" + idosdetails + ", osname=" + osname + ", osdetails=" + osdetails + ", status="
			+ status + ", creationtime=" + creationtime + ", createdby=" + createdby + ", modificationtime="
			+ modificationtime + ", modifiedby=" + modifiedby + "]";
}
	

	
}
